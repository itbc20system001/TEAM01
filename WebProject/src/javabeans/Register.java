package javabeans;

public class Register {
	private String l_name ;
	private String f_name ;
	private String l_name_kana ;
	private String f_name_kana ;
	private String prefecture ;
	private String city ;
	private String o_address ;
	private String tel;
	private String email ;
	private String birthday;
	private String password ;

	public String getL_name() {
		return l_name;
	}
	public void setL_name(String l_name) {
		this.l_name = l_name;
	}
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getL_name_kana() {
		return l_name_kana;
	}
	public void setL_name_kana(String l_name_kana) {
		this.l_name_kana = l_name_kana;
	}
	public String getF_name_kana() {
		return f_name_kana;
	}
	public void setF_name_kana(String f_name_kana) {
		this.f_name_kana = f_name_kana;
	}
	public String getPrefecture() {
		return prefecture;
	}
	public void setPrefecture(String prefecture) {
		this.prefecture = prefecture;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getO_address() {
		return o_address;
	}
	public void setO_address(String o_address) {
		this.o_address = o_address;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}

}
